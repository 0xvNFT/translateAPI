package com.dichthuatjun88binh.jun88.model;

public class LanguageModel {

    String language_name;
    int language_flag;
    String language_code;
    String language_speech_code;

    public String getLanguage_name() {
        return language_name;
    }

    public void setLanguage_name(String language_name) {
        this.language_name = language_name;
    }

    public int getLanguage_flag() {
        return language_flag;
    }

    public void setLanguage_flag(int language_flag) {
        this.language_flag = language_flag;
    }

    public String getLanguage_code() {
        return language_code;
    }

    public void setLanguage_code(String language_code) {
        this.language_code = language_code;
    }

    public String getLanguage_speech_code() {
        return language_speech_code;
    }

    public void setLanguage_speech_code(String language_speech_code) {
        this.language_speech_code = language_speech_code;
    }
}
